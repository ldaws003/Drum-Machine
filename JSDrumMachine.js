
$(document).keydown(function(e){
    switch(e.key){
      case 'q':
      case 'Q':
        $('#Q').trigger('click');
        break;
      case 'w':
      case 'W':
        $('#W').trigger('click');
        break;
      case 'e':
      case 'E':
        $('#E').trigger('click');
        break;
      case 'a':
      case 'A':
        $('#A').trigger('click');
        break;
      case 's':
      case 'S':
        $('#S').trigger('click');
        break;
      case 'd':
      case 'D':
        $('#D').trigger('click');
        break;
      case 'z':
      case 'Z':
        $('#Z').trigger('click');
        break;
      case 'x':
      case 'X':
        $('#X').trigger('click');
        break;
      case 'c':
      case 'C':
        $('#C').trigger('click');
        break;
    }
});
